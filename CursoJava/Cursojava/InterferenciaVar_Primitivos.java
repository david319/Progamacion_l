public class InterferenciaVar_Primitivos {

    public static void main(String[] args) {

        var a = 10;
        System.out.println("Numero Entero = " + a);

        var b = 10.0;
        System.out.println("Numero Double = " + b);

        var c = 10.0f;
        System.out.println("Numero Float = " + c);

    }

}
